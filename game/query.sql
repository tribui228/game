DROP DATABASE IF EXISTS game;
CREATE DATABASE IF NOT EXISTS game CHARACTER SET utf8 COLLATE utf8_general_ci;

USE game;

CREATE TABLE IF NOT EXISTS genre(
	name VARCHAR(255) UNIQUE NOT NULL,
	display VARCHAR(255),
	PRIMARY KEY (name)
);

CREATE TABLE IF NOT EXISTS system_requirements(
	id INT UNIQUE AUTO_INCREMENT NOT NULL,
	OS varchar(255),
	processor varchar(255),
	memory varchar(255),
	graphics varchar(255),
	sound_card varchar(255),
	storage varchar(255),
	PRIMARY KEY (id)
);

CREATE TABLE IF NOT EXISTS sale(
	name VARCHAR(255) UNIQUE NOT NULL,
	display VARCHAR(255) UNIQUE,
	discount DECIMAL(3,2),
	date_begin DATETIME,
	date_end DATETIME
);

CREATE TABLE IF NOT EXISTS product(
	name VARCHAR(255) UNIQUE NOT NULL,
	min_system_require_id INT,
	rec_system_require_id INT,
	display VARCHAR(255),
	developer VARCHAR(255),
	publisher VARCHAR(255),
	release_date DATE,
	price DECIMAL(16,2),
	sale_name VARCHAR(255),
	image VARCHAR(255),
	background_image VARCHAR(255),
	description TEXT,
	PRIMARY KEY (name),
	CONSTRAINT product_fk_min FOREIGN KEY(min_system_require_id) REFERENCES system_requirements(id),
	CONSTRAINT product_fk_rec FOREIGN KEY(rec_system_require_id) REFERENCES system_requirements(id),
	CONSTRAINT product_fk_sale FOREIGN KEY(sale_name) REFERENCES sale(name)
);

CREATE TABLE IF NOT EXISTS product_genre(
	product_name VARCHAR(255),
	genre_name VARCHAR(255),
	CONSTRAINT product_genre_pk PRIMARY KEY(product_name, genre_name),
	CONSTRAINT product_genre_fk_product FOREIGN KEY(product_name) REFERENCES product(name),
	CONSTRAINT product_genre_fk_genre FOREIGN KEY(genre_name) REFERENCES genre(name)
);

CREATE TABLE IF NOT EXISTS product_code(
	id INT UNIQUE NOT NULL AUTO_INCREMENT,
	product_name VARCHAR(255),
	code VARCHAR(255),
	status TINYINT(1),
	date_added DATETIME,
	CONSTRAINT product_code_pk PRIMARY KEY(id),
	CONSTRAINT product_code_fk_name FOREIGN KEY(product_name) REFERENCES product(name)
);

CREATE TABLE IF NOT EXISTS `group`(
	name VARCHAR(255) UNIQUE NOT NULL,
	display VARCHAR(255),
	PRIMARY KEY (name)
);

CREATE TABLE IF NOT EXISTS group_permission(
	id INT UNIQUE NOT NULL AUTO_INCREMENT,
	`group` VARCHAR(255) NOT NULL,
	permission VARCHAR(255),
	value TINYINT(1),
	CONSTRAINT group_permission_pk PRIMARY KEY(id),
	CONSTRAINT group_permission_fk_name FOREIGN KEY(`group`) REFERENCES `group`(name)
);

CREATE TABLE IF NOT EXISTS `user`(
	username VARCHAR(255) UNIQUE NOT NULL,
	`group` VARCHAR(255) NOT NULL,
	password VARCHAR(255),
	firstname VARCHAR(255),
	lastname VARCHAR(255),
	birth DATE,
	address VARCHAR(255),
	phone VARCHAR(20),
	email VARCHAR(255),
	CONSTRAINT user_permission_pk PRIMARY KEY(username),
	CONSTRAINT user_permission_fk_group FOREIGN KEY(`group`) REFERENCES `group`(name)
);

CREATE TABLE IF NOT EXISTS user_permission(
	id INT UNIQUE NOT NULL AUTO_INCREMENT,
	username VARCHAR(255),
	permission VARCHAR(255),
	value TINYINT(1),
	CONSTRAINT user_permission_pk PRIMARY KEY(id),
	CONSTRAINT user_permission_fk_name FOREIGN KEY(username) REFERENCES user(username)
);

CREATE TABLE IF NOT EXISTS `order`(
	id INT UNIQUE AUTO_INCREMENT NOT NULL,
	username VARCHAR(255),
	total_price DECIMAL(16,2),
	quantity INT,
	status TINYINT(1),
	date_ordered DATETIME,
	date_checked DATETIME,
	PRIMARY KEY(id),
	FOREIGN KEY(username) REFERENCES user(username)
);

CREATE TABLE IF NOT EXISTS order_item(
	id INT UNIQUE AUTO_INCREMENT NOT NULL,
	order_id INT,
	product_name VARCHAR(255),
	code VARCHAR(255),
	price DECIMAL(16,2),
	discount DECIMAL(3,2),
	CONSTRAINT order_item_pk PRIMARY KEY(id),
	CONSTRAINT order_item_fk_order FOREIGN KEY(order_id) REFERENCES `order`(id)
);

